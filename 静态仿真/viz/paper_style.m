function paper_style()
%PAPER_STYLE Set global plotting style for paper-quality figures.

set(groot, 'defaultFigureColor', 'w');
set(groot, 'defaultAxesFontName', 'Times New Roman');
set(groot, 'defaultTextFontName', 'Times New Roman');
set(groot, 'defaultAxesFontSize', 12.5);
set(groot, 'defaultTextFontSize', 12.5);
set(groot, 'defaultAxesLineWidth', 1.15);
set(groot, 'defaultLineLineWidth', 2.0);
set(groot, 'defaultAxesBox', 'on');
set(groot, 'defaultAxesGridAlpha', 0.12);
set(groot, 'defaultAxesMinorGridAlpha', 0.06);
set(groot, 'defaultLegendBox', 'off');
set(groot, 'defaultAxesTickDir', 'out');
set(groot, 'defaultAxesLayer', 'top');

end
